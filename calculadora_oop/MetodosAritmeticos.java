package calculadora;

public class MetodosAritmeticos extends Propiedades {

	public double suma(double num1, double num2, double rs) {
		setResultados(rs);
		setNum1(num1);
		setNum2(num2);

		return getResultados();
	}

	public double resta(double num1, double num2, double rs) {
		setResultados(rs);
		setNum1(num1);
		setNum2(num2);

		return getResultados();
	}

	public double multi(double num1, double num2, double rs) {
		setResultados(rs);
		setNum1(num1);
		setNum2(num2);

		return getResultados();
	}

	public double divid(double num1, double num2, double rs) {
		setResultados(rs);
		setNum1(num1);
		setNum2(num2);

		return getResultados();
	}

}
